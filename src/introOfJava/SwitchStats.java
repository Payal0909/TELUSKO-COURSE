package introOfJava;

public class SwitchStats {

    public static void main(String[] args) {

//        switch statements

        int n=2;
        switch (n)
        {
            case 1:
                System.out.println("Dinky");
                break;
            case 2 :
                System.out.println("Simmy");
                break;
            default :
            System.out.println("came out of Switch");

        }
    }
}
